    private void SelectProdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SelectProdButtonActionPerformed
        InputStream input;
        FileOutputStream output;
        int q = Integer.parseInt(uppid.getText());
        try {
            Statement s  = dbcp.poolCon().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM `Products` WHERE product_id="+q);
            File theFile2 = new File("temp2.png");
            output = new FileOutputStream(theFile2);
            
        if (rs.next()){    
            upname.setText(rs.getString("product_name"));
            upprice.setText(rs.getString("product_price"));
            upcategory.setText(rs.getString("product_category"));
            updescription.setText(rs.getString("product_description"));
                
            input = rs.getBinaryStream("product_picture");
            byte buffer[] = new byte[1024];
            while (input.read(buffer)>0){
                output.write(buffer);
            }
            String path = theFile2.getAbsolutePath();
            ImageIcon myImage = new ImageIcon(path);
            Image img = myImage.getImage();
            Image newImg = img.getScaledInstance(uppicture.getWidth(), uppicture.getHeight(), SCALE_SMOOTH);
            ImageIcon image = new ImageIcon(newImg);
            uppicture.setIcon(image);
        rs.close();
        s.close();
        }
        else {
            JOptionPane.showMessageDialog(null, "Produs inexistent", "EROARE", JOptionPane.INFORMATION_MESSAGE);
        }
        }
        catch (Exception e){
        }
    }//GEN-LAST:event_SelectProdButtonActionPerformed

