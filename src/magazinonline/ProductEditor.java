    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser jfile = new JFileChooser();
        jfile.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Image","jpg","png");
        jfile.addChoosableFileFilter(filter);
        
        int result = jfile.showSaveDialog(this);
        File selectedFile = jfile.getSelectedFile();
        String path = selectedFile.getAbsolutePath();
        ImageIcon myImage = new ImageIcon(path);
        Image Img = myImage.getImage();
        Image newImage = Img.getScaledInstance(uppicture.getWidth(), uppicture.getHeight(), SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImage);
        addpicture.setIcon(image);
        
        FileInputStream fis = null;
        try {
            String category;
            String name;
            String price;
            String description;
            category = addcategory.getText();
            name = addname.getText();
            price = addprice.getText();
            description = adddesc.getText();
            
            fis = new FileInputStream(path);
            PreparedStatement ps  = dbcp.poolCon().prepareStatement("INSERT INTO Products (product_id, product_category, product_name, product_price, product_description, product_picture) VALUES "
                    + "(DEFAULT, '"+category+"', '"+name+"', '"+price+"', '"+description+"', ?)");
            ps.setBinaryStream(1, fis);
            ps.executeUpdate();
            ps.close();
            JOptionPane.showMessageDialog(null, "Produs adaugat cu succes!", "", JOptionPane.INFORMATION_MESSAGE);
        }
        catch (Exception e){
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

